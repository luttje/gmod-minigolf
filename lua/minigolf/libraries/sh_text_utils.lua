Minigolf.Text = Minigolf.Text or {}

-- Pluralizes text depending on the amount
function Minigolf.Text.Pluralize(text, amount)
	return amount == 1 and text or text .. "s"
end

local badWords -- See end of file for word list

-- Returns true if any of the words are a bad word, secondly returns the bad word
function Minigolf.Text.ContainsBadWords(text)
	text = string.lower(text)
	text = string.Replace(text, "_", " ")

	if (badWords[text]) then
		return true, text
	end

	local seperatedWords = string.Explode(" ", text)

	for _, word in pairs(seperatedWords) do
		if (badWords[word]) then
			return true, word
		end
	end

	--[[
    Things this doesn't catch:
      - Combined swear words
      - Swear words not in the list
      - Swear words seperated by something other than spaces or underscores
      - Swear words wr_it_ten lik_e thi_s
      - etc. etc. etc. I'm sure players can think of more ways
  ]] --

	return false
end

